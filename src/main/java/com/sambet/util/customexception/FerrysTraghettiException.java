package com.sambet.util.customexception;


/**
 * @author Matteo - matteo.manili@gmail.com
 *
 */
public class FerrysTraghettiException extends Exception {
	private static final long serialVersionUID = 7924574978907048185L;

	public FerrysTraghettiException() {
		super();
	}

	public FerrysTraghettiException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FerrysTraghettiException(String message, Throwable cause) {
		super(message, cause);
	}

	public FerrysTraghettiException(String message) {
		super(message);
	}

	public FerrysTraghettiException(Throwable cause) {
		super(cause);
	}


	
	
}
